(function($) {
	$.extend($,{
		//强制刷新,可以具体执行相应控件的refresh事件
		refresh:function(jqmObj){
			if(!jqmObj)
				return;
			var obj = $(jqmObj);
//			obj.find("select").selectpicker('refresh');
			obj.find("select").addClass("styled-select");
		}
	});
})(jQuery);